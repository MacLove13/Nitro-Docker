-- Furniture Creator: furnis_custom table migration
-- This table stores custom furniture creation jobs

CREATE TABLE IF NOT EXISTS `furnis_custom` (
  `id`                      int          NOT NULL AUTO_INCREMENT,
  `public_name`             varchar(100) NOT NULL,
  `item_name`               varchar(100) DEFAULT NULL COMMENT 'Generated by furni-generator',
  `sprite_id`               int          DEFAULT NULL COMMENT 'Generated by furni-generator',
  `type`                    varchar(3)   NOT NULL DEFAULT 's' COMMENT 's=floor, i=wall',
  `width`                   int          NOT NULL DEFAULT '1',
  `length`                  int          NOT NULL DEFAULT '1',
  `stack_height`            double(4,2)  NOT NULL DEFAULT '1.00',
  `allow_stack`             tinyint(1)   NOT NULL DEFAULT '1',
  `allow_sit`               tinyint(1)   NOT NULL DEFAULT '0',
  `allow_lay`               tinyint(1)   NOT NULL DEFAULT '0',
  `allow_walk`              tinyint(1)   NOT NULL DEFAULT '0',
  `allow_gift`              tinyint(1)   NOT NULL DEFAULT '1',
  `allow_trade`             tinyint(1)   NOT NULL DEFAULT '1',
  `allow_recycle`           tinyint(1)   NOT NULL DEFAULT '0',
  `allow_marketplace_sell`  tinyint(1)   NOT NULL DEFAULT '0',
  `allow_inventory_stack`   tinyint(1)   NOT NULL DEFAULT '1',
  `interaction_type`        varchar(100) NOT NULL DEFAULT 'default',
  `interaction_modes_count` int          NOT NULL DEFAULT '1',
  `page_id`                 int          NOT NULL DEFAULT '1',
  `catalog_name`            varchar(100) NOT NULL DEFAULT '',
  `cost_credits`            int          NOT NULL DEFAULT '0',
  `cost_points`             int          NOT NULL DEFAULT '0',
  `points_type`             int          NOT NULL DEFAULT '0',
  `amount`                  int          NOT NULL DEFAULT '1',
  `limited_sells`           int          NOT NULL DEFAULT '0',
  `limited_stack`           int          NOT NULL DEFAULT '0',
  `images`                  text         DEFAULT NULL COMMENT 'JSON array of uploaded image paths keyed by rotation/state',
  `nitro_file`              varchar(255) DEFAULT NULL COMMENT 'Relative path to generated .nitro file',
  `swf_file`                varchar(255) DEFAULT NULL COMMENT 'Relative path to generated .swf file',
  `status`                  enum('processing','pending','done') NOT NULL DEFAULT 'processing',
  `created_at`              timestamp    NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at`              timestamp    NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Custom furniture creation jobs';
